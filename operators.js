//arithmetic operators
let x = 10;

let y = 5;
console.log(x + y); //addition
console.log(x - y); // substraction
console.log(x * y); // multiplication
console.log(x % y); // modulo division
console.log(x / y); // division
console.log(++x); // pre-increment
console.log(--x); // pre-decrement
console.log(x++); // post increment

//comparison operators

console.log(10 > 5); //true
console.log(10 < 5); //false
console.log(10 == 10); //true
console.log(10 === 10); //true
console.log(10 === "10"); //false  it compares type and value
console.log(10 !== "10"); //true it compares type and value

//logical operators

console.log(true && true); //true
console.log(false && false); // false
console.log(true && false); //false
console.log(false && true); //false
console.log(true || true); //true
console.log(false || true); //true
console.log(!false); //true

